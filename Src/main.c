/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */
  uint16_t raw_data; // Temperature Sensor Analog reading for ADC
  float temp; // The temperature reading in degrees Fahrenheit
  uint8_t fan_on = 0; // Flag for whether fan on or off
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_TIM3_Init();
  MX_I2C1_Init();
  MX_ADC1_Init();
  MX_TIM4_Init();
  MX_TIM5_Init();
  MX_TIM9_Init();
  MX_TIM2_Init();
  MX_TIM10_Init();
  MX_TIM12_Init();
  /* USER CODE BEGIN 2 */
  // Timer pauses when debug suspends
  DBGMCU->APB1FZ |= DBGMCU_APB1_FZ_DBG_TIM4_STOP;
  DBGMCU->APB1FZ |= DBGMCU_APB1_FZ_DBG_TIM5_STOP;
  DBGMCU->APB2FZ |= DBGMCU_APB2_FZ_DBG_TIM9_STOP;


  // Initialize LCD
  LCD_Init(&hi2c1);
  LCD_Send_String(&hi2c1, "Initializing...");
  HAL_Delay(500);
  LCD_Clear(&hi2c1);
  HAL_Delay(400);

  // Initialize Motor Driver PWM Signal
  TIM12->CCR1 = 0;

  // Put Servo in starting position
  TIM3->CCR2 = 25;
  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_2);
  HAL_Delay(20);
  HAL_TIM_PWM_Stop(&htim3, TIM_CHANNEL_2);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */

	/**
	 * Temperature Reading
	 */

	// Poll for temperature. poll_temp is set every 30s in TIM9 Interrupt
	if (poll_temp)
	{
		// *********** CHECK THE RETURN VALUES THE STATUS for more robust ***************
		HAL_ADC_Start(&hadc1);
		HAL_ADC_PollForConversion(&hadc1, 10);
		raw_data = HAL_ADC_GetValue(&hadc1);
		temp = convert_temp(raw_data);
		temp = roundf(temp * 10) / 10; // holds temperature DD.d degrees F


		// Limit to 1 decimal place
		float temperature_val = 10 * temp;
		unsigned short digits = (unsigned int)temperature_val/10;
		unsigned short tenths_place_val = (unsigned int)temperature_val % 10;

		// Get temperature as formatted string
		char temp_str[12];
		sprintf(temp_str, "%d.%d", digits, tenths_place_val);
		char prev_temp[12];
		strcpy(prev_temp, temp_str);

		/**
		 * Display temperature on LCD
		 */

		// Write temperature to display
		LCD_Cursor_Set(&hi2c1, 0,0);
		LCD_Send_String(&hi2c1, temp_str);

		// Disable temperature polling for 30s
		poll_temp = 0;
		HAL_TIM_OC_Start_IT(&htim9, TIM_CHANNEL_1);


		if (!fan_on && (temp > FAN_LEVEL_1_THRESHOLD))
		{
			// start servo rotation
			TIM3->CCR2 = pwm_width;
			HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_2);
			HAL_TIM_OC_Start_IT(&htim4, TIM_CHANNEL_1);

			// Fan is on
			fan_on = 1;

			// Disable turning fan off until minimum 60s
			fan_change_en = 0;

			// Start 60s timer until fan can turn off
			HAL_TIM_OC_Start_IT(&htim5, TIM_CHANNEL_1);

			/* Start Motor */

			// Start Motor Encoder
			HAL_TIM_Encoder_Start(&htim2, TIM_CHANNEL_ALL);

			// Set fan level and start PID Controller
			fan_level_ref = FAN_LEVEL_1;
			if (temp > FAN_LEVEL_3_THRESHOLD)
			{
				fan_level_ref = FAN_LEVEL_3;
				TIM12->CCR1 = 600;
			}
			else if (temp > FAN_LEVEL_2_THRESHOLD)
			{
				fan_level_ref = FAN_LEVEL_2;
				TIM12->CCR1 = 1000;
			}

			// Sample encoders every 100ms
			HAL_TIM_OC_Start_IT(&htim10, TIM_CHANNEL_1);

			// Start Motor Driver PWM Signal
			HAL_TIM_PWM_Start(&htim12, TIM_CHANNEL_1);
		}

		// Stop fan if it's cool and the fan is on
		else if (fan_change_en && fan_on && (temp <= FAN_LEVEL_1_THRESHOLD))
		{
			// Stop servo rotation
			HAL_TIM_PWM_Stop(&htim3, TIM_CHANNEL_2);
			HAL_TIM_OC_Stop_IT(&htim4, TIM_CHANNEL_1);

			// Fan is off
			fan_on = 0;

			// Stop encoder sampling
			HAL_TIM_OC_Stop_IT(&htim10, TIM_CHANNEL_1);

			// Stop Motor driver PWM Signal
			TIM12->CCR1 = 0;
			HAL_TIM_PWM_Stop(&htim12, TIM_CHANNEL_1);
		}
	}
  }
  /* USER CODE END 3 */
}

